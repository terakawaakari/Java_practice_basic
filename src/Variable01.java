
public class Variable01 {
	public static void main(String[] args) {
		int num1;
		String str1;
		num1 = 10;
		str1 = "Hello World!";
		System.out.println(num1);
		System.out.println(str1);

		num1 = 20;
		System.out.println(num1);

		int num2 = 30;
		System.out.println(num2);

		String name = "山田花子";
		System.out.println(name);

		name = "山田太郎";
		System.out.println(name);

		int LACKEY_NUM = 7;
		System.out.println(LACKEY_NUM);
	}
}
